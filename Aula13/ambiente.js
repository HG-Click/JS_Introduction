/* ESTRUTURA DE REPETIÇÃO COM TESTE LOGICO NO INCIO */
var c = 1
while (c <= 6) {
    console.log (`Passo ${c}`)
    c++
}

/* ESTRUTURA DE REPETIÇÃO COM TESTE LOGICO NO FINAL */
var c = 1
do {
    console.log (`Passo ${c}`)
    c++
} while (c <= 10)

/*ESTRTURA DE REPETIÇÃO COM VARIAVEL DE CONTROLE */
for (var c = 1;c <= 4;c++) {
    console.log(c)
}





